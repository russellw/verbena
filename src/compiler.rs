use crate::ast::*;
use crate::compile_error::*;
use crate::error_context::*;
use crate::program::*;
use crate::val::*;
use num_bigint::BigInt;
use num_traits::Num;
use std::collections::HashMap;
use std::mem;
use std::str::FromStr;

// Branches can go forward as well as back
// so can only be resolved at the end, when all labels have been seen
// so need to keep track until then
struct Branch {
    // A branch might find no corresponding label
    // and need to report error
    ec: ErrorContext,

    // Index in the code vector
    i: usize,

    // Label referred to
    // This may be a label explicitly written by the programmer
    // or automatically generated by the compiler
    label: String,
}

struct Compiler<'a> {
    // TODO: Does this still need to be a field?
    ast: &'a AST,

    // Counter for generating temporary names
    tmp_count: usize,

    branches: Vec<Branch>,
    labels: HashMap<String, usize>,

    code: Vec<Inst>,
    ecs: Vec<ErrorContext>,
}

fn parse_bigint(input: &str) -> Result<BigInt, String> {
    // Convert to bytes for easier prefix checking
    let bytes = input.as_bytes();

    // Check for prefixes and determine base
    if bytes.len() >= 2 && bytes[0] == b'0' {
        match bytes[1] {
            b'x' | b'X' => {
                // Hexadecimal (0x)
                let hex_str = &input[2..]; // Skip the '0x' prefix
                return BigInt::from_str_radix(hex_str, 16)
                    .map_err(|e| format!("Invalid hexadecimal: {}", e));
            }
            b'b' | b'B' => {
                // Binary (0b)
                let bin_str = &input[2..]; // Skip the '0b' prefix
                return BigInt::from_str_radix(bin_str, 2)
                    .map_err(|e| format!("Invalid binary: {}", e));
            }
            b'o' | b'O' => {
                // Octal (0o)
                let oct_str = &input[2..]; // Skip the '0o' prefix
                return BigInt::from_str_radix(oct_str, 8)
                    .map_err(|e| format!("Invalid octal: {}", e));
            }
            _ => {}
        }
    }

    // Default to decimal
    BigInt::from_str(input).map_err(|e| format!("Invalid decimal: {}", e))
}

impl<'a> Compiler<'a> {
    fn new(ast: &'a AST) -> Self {
        Compiler {
            ast,
            tmp_count: 0,
            labels: HashMap::<String, usize>::new(),
            branches: Vec::<Branch>::new(),
            code: Vec::<Inst>::new(),
            ecs: Vec::<ErrorContext>::new(),
        }
    }

    fn tmp(&mut self) -> String {
        self.tmp_count += 1;

        // Temporary names cannot clash with any valid identifier
        format!(" {}", self.tmp_count)
    }

    fn add(&mut self, ec: &ErrorContext, inst: Inst) {
        self.code.push(inst);
        self.ecs.push(ec.clone());
    }

    fn branch(&mut self, ec: &ErrorContext, br: Inst, label: &str) {
        self.branches.push(Branch {
            ec: ec.clone(),
            i: self.code.len(),
            label: label.to_string(),
        });
        self.add(ec, br);
    }

    fn expr(&mut self, a: &Expr) -> Result<(), CompileError> {
        match a {
            Expr::True => {
                self.add(&ErrorContext::blank(), Inst::Const(Val::True));
            }
            Expr::False => {
                self.add(&ErrorContext::blank(), Inst::Const(Val::False));
            }
            Expr::Null => {
                self.add(&ErrorContext::blank(), Inst::Const(Val::Null));
            }
            Expr::Str(s) => {
                self.add(
                    &ErrorContext::blank(),
                    Inst::Const(Val::Str(s.clone().into())),
                );
            }
            Expr::Float(ec, s) => {
                let s = s.replace('_', "");
                let a = match s.parse::<f64>() {
                    Ok(a) => a,
                    Err(e) => return Err(CompileError::new(ec.clone(), e.to_string())),
                };
                self.add(ec, Inst::Const(Val::Float(a)));
            }
            Expr::Int(ec, s) => {
                let a = match parse_bigint(s) {
                    Ok(a) => a,
                    Err(e) => return Err(CompileError::new(ec.clone(), e.to_string())),
                };
                self.add(ec, Inst::Const(Val::Int(a)));
            }
            Expr::Call(ec, f, args) => {
                if let Expr::Id(_, name) = &**f {
                    for a in args {
                        self.expr(a)?;
                    }
                    self.add(ec, Inst::Call(name.to_string(), args.len()));
                    return Ok(());
                }
                self.expr(f)?;
                for a in args {
                    self.expr(a)?;
                }
                self.add(ec, Inst::CallIndirect(args.len()));
            }
            Expr::Id(ec, name) => {
                self.add(ec, Inst::Load(name.to_string()));
            }
            Expr::Infix(ec, inst, a, b) => {
                self.expr(a)?;
                self.expr(b)?;
                self.add(ec, inst.clone());
            }
            Expr::Prefix(ec, inst, a) => {
                self.expr(a)?;
                self.add(ec, inst.clone());
            }
            Expr::Assign(ec, a, b) => match &**a {
                Expr::Id(_, name) => {
                    self.expr(b)?;
                    self.add(ec, Inst::Store(name.to_string()));
                }
                Expr::Call(ec, a, indexes) => {
                    if indexes.len() != 1 {
                        return Err(CompileError::new(
                            ec.clone(),
                            "Expected one index".to_string(),
                        ));
                    }
                    self.expr(a)?;
                    self.expr(&indexes[0])?;
                    self.expr(b)?;
                    self.add(ec, Inst::StoreAt);
                }
                _ => {
                    eprintln!("{:?}", a);
                    eprintln!("{:?}", b);
                    todo!();
                }
            },
            _ => {
                eprintln!("{:?}", a);
                todo!();
            }
        }
        Ok(())
    }

    fn stmt(&mut self, a: &Stmt) -> Result<(), CompileError> {
        match a {
            Stmt::Assert(ec, cond, msg) => {
                self.expr(cond)?;
                self.add(ec, Inst::Assert(msg.to_string()));
            }
            Stmt::Print(ec, v) => {
                for a in v {
                    self.expr(a)?;
                    self.add(ec, Inst::Call("_print".to_string(), 1));
                }
            }
            Stmt::Label(ec, s) => match self.labels.insert(s.to_string(), self.code.len()) {
                Some(_) => {
                    return Err(CompileError::new(
                        ec.clone(),
                        format!("'{}' was already defined", s),
                    ));
                }
                _ => {}
            },
            Stmt::Goto(ec, label) => self.branch(ec, Inst::Br(0), label),
            Stmt::DoWhile(cond, body) => {
                // Body
                let loop_label = self.tmp();
                self.labels.insert(loop_label.clone(), self.code.len());
                self.block(body)?;

                // Condition
                self.expr(cond)?;
                self.branch(&ErrorContext::blank(), Inst::BrTrue(0), &loop_label);
            }
            Stmt::While(cond, body) => {
                // Bypass
                let cond_label = self.tmp();
                self.branch(&ErrorContext::blank(), Inst::Br(0), &cond_label);

                // Body
                let loop_label = self.tmp();
                self.labels.insert(loop_label.clone(), self.code.len());
                self.block(body)?;

                // Condition
                self.labels.insert(cond_label, self.code.len());
                self.expr(cond)?;
                self.branch(&ErrorContext::blank(), Inst::BrTrue(0), &loop_label);
            }
            Stmt::If(cond, yes, no) => {
                // Condition
                self.expr(cond)?;
                let else_label = self.tmp();
                self.branch(&ErrorContext::blank(), Inst::BrFalse(0), &else_label);

                // Then
                self.block(yes)?;
                let after_label = self.tmp();
                self.branch(&ErrorContext::blank(), Inst::Br(0), &after_label);

                // Else
                self.labels.insert(else_label, self.code.len());
                self.block(no)?;

                // After
                self.labels.insert(after_label, self.code.len());
            }
            Stmt::Expr(a) => {
                self.expr(a)?;
                self.add(&ErrorContext::blank(), Inst::Pop);
            }
            _ => {
                eprintln!("{:?}", a);
                todo!();
            }
        }
        Ok(())
    }

    fn block(&mut self, v: &Vec<Stmt>) -> Result<(), CompileError> {
        for a in v {
            self.stmt(&a)?;
        }
        Ok(())
    }

    fn compile(&mut self) -> Result<Program, CompileError> {
        // Generate code
        self.block(&self.ast.code)?;

        // Resolve branches
        for branch in &self.branches {
            let target = match self.labels.get(&branch.label) {
                Some(i) => *i,
                None => {
                    return Err(CompileError::new(
                        branch.ec.clone(),
                        format!("'{}' not found", branch.label),
                    ));
                }
            };
            let br = &self.code[branch.i];
            self.code[branch.i] = match br {
                Inst::Br(_) => Inst::Br(target),
                Inst::BrFalse(_) => Inst::BrFalse(target),
                Inst::BrTrue(_) => Inst::BrTrue(target),
                _ => {
                    eprintln!("{:?}", br);
                    todo!();
                }
            };
        }

        Ok(Program {
            code: mem::take(&mut self.code),
            ecs: mem::take(&mut self.ecs),
        })
    }
}

pub fn compile(ast: &AST) -> Result<Program, CompileError> {
    let mut compiler = Compiler::new(ast);
    compiler.compile()
}
